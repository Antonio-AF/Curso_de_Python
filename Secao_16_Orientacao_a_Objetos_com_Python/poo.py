"""

Programação Orientada a Objetos - POO


-> POO é um paradigma de programação que utiliza o mapemamento de objetos do mundo real
para modelos computacionais.

- Paradigma de programação é a forma/metodologia utilizada para pensar/desenvolver sistemas.

Principais elementos da Orientação a Objetos.s

- Classe -> Modelo do objeto do mundo real sendo representado computacionalmente;
- Atributos -> Características do objetos;
- Métodos -> Comportamento do objeto;
- Construtor -> Método especial utilizado para criar os objetos;
- Objetos -> Instância da Classe.A


"""

numero = 10

print(numero)
print(type(numero))

nome = 'Geek'
print(nome)
print(type(nome))


